# Single Data Types (Single Value)

# 🔢 1. Integer (int) – whole number
age = 25
print(age)
print(type(age))


# 🌡️ Float
price = 99.99
print(price)
print(type(price))



# 🔤 String
name = "Sarwesh"
print(name)
print(type(name))



# 🔘 Boolean
is_pass = True
print(is_pass)
print(type(is_pass))



# ......................................................................


# 📋 List
fruits = ["apple", "banana"]
print(fruits)
print(type(fruits))



# 🔒 Tuple
data = ("apple", "banana")
print(data)
print(type(data))



# 🚫 Set
numbers = {1, 2, 3}
print(numbers)
print(type(numbers))



# 🔑 Dictionary
student = {"name": "Govind"}
print(student)
print(type(student))