# For Loop

# Q1. Write a Python program to print numbers from 0 to 4 using for loop.

for i in range(5):
    print(i)


print(" .............................end .............................. ")


# Q2. Write a Python program to print numbers from 1 to 5 using for loop.
for i in range(1, 6):
    print(i)


print(" .............................end .............................. ")


# Q3. Write a Python program to print each character of a string using for loop.
name = "Rahul"
for ch in name:
    print(ch)


print(" .............................end .............................. ")

# Q4. Print even numbers (0 to 10)
for i in range(0, 11, 2):
    print(i)   # i = 0,2,4,6,8,10


print(" .............................end .............................. ")

# Q5. Print reverse numbers (5 to 1)
for i in range(5, 0, -1):
    print(i)   # i = 5,4,3,2,1


print(" .............................end .............................. ")